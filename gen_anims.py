"""Animation generator. Spec→JSON. Each frame is 8 strings of 8 chars (palette index 0-9)."""
import json, os, sys, pathlib

BASE = pathlib.Path(__file__).parent / "examples"


def write(category: str, name: str, palette, frames, delay=220, loop=True):
    """frames: list of 8-line strings (each line 8 chars '0'..'9')."""
    pixels = []
    for fr in frames:
        rows = fr.strip().split("\n")
        assert len(rows) == 8, f"{name}: need 8 rows got {len(rows)}"
        flat = []
        for row in rows:
            row = row.strip()
            assert len(row) == 8, f"{name}: row not 8 chars: {row!r}"
            for ch in row:
                flat.append(int(ch))
        pixels.append(flat)
    out = {
        "version": 1,
        "palette": [list(c) for c in palette],
        "frames": pixels,
        "frame_delay_ms": delay,
        "loop": loop,
    }
    target = BASE / category / f"{name}.json"
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(json.dumps(out, indent=2))


