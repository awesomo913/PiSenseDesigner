"""Massive 8x8 stamp library — categorized icons for the editor.

Each stamp is a 64-character string of '1's (paint with selected color)
and '0's (paint with background color, palette index 0). Read row-major,
top-left to bottom-right, 8 rows of 8 chars.
"""

from __future__ import annotations

from typing import Dict


def _s(*rows: str) -> str:
    """Helper to keep stamp strings readable. Concatenates 8 rows."""
    assert len(rows) == 8, f"need 8 rows, got {len(rows)}"
    for r in rows:
        assert len(r) == 8, f"row not 8 chars: {r!r}"
    return "".join(rows)


